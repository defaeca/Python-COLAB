# Faça um programa que peça abase e a altura de um retângulo e
# calcule e mostre na tela a área e o perímetro.

b = float(input("Digite o valor da base do retangulo: "))
h = float(input("Digite o valor da altura do retangulo: "))

p = 2*(b+h)
a = b*h

print(f"A Area do rentangulo é {a} e o perimentro do retangulo é {p}")
